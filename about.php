<h1>About Jonny's flashcards</h1>

<p>This is a simple web application that simulates flashcards, hopefully
useful for learning vocabulary. The computer will test you by showing you
cards. You can tell the computer whether you knew what was on the other
side. The computer will try to show you cards that you don't know well
and also cards that haven't come up in a while.</p>

